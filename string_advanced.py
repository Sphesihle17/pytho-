#Advanced concepts in string

#indexing
message ="   My output, statement   "
print(message[2])
print(message[-1])

# finding the length of our string
print(len(message))

#using Strip method
print(message.strip())

#converting to lower and upper case
print(message.lower())
print(message.upper())

#using split 
print(message.split(','))

#using replace
print(message.replace(message,"Hello world"))

